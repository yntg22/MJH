package kr.or.ddit.vo;

import java.io.Serializable;

/**
 * Domain Layer : JavaBean 규약
 *
 */
public class DataBasePropertyVO implements Serializable{
	
	private String propertyName;
	private String propertyValue;
	private String description;
	
	public String getPropertyName() {
		return propertyName;
	}
	public void setPropertyName(String propertyName) {
		this.propertyName = propertyName;
	}
	public String getPropertyValue() {
		return propertyValue;
	}
	public void setPropertyValue(String propertyValue) {
		this.propertyValue = propertyValue;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((propertyName == null) ? 0 : propertyName.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		DataBasePropertyVO other = (DataBasePropertyVO) obj;
		if (propertyName == null) {
			if (other.propertyName != null)
				return false;
		} else if (!propertyName.equals(other.propertyName))
			return false;
		return true;
	}
	@Override
	public String toString() {
		return "DataBasePropertyVO [propertyName=" + propertyName + ", propertyValue=" + propertyValue
				+ ", description=" + description + "]";
	}
	
	
}
